				<p>
					O Coaching inicia-se com o estabelecimento de uma parceria forte entre o Coachee (aluno ou orientando) e o Mentor Coach (mentor, orientador ou treinador). Na sequência, tendo por base os objetivos do Coachee definidos, o Mentor-Coach, utilizando-se de ferramentas apropriadas, aferirá o nível e o perfil do aluno a ser trabalhado, definindo seu “status” atual em cada disciplina. Ato contínuo, o Coach estabelecerá o planejamento, as diretrizes e as metas a serem alcançadas. 
				</p>
				<p>
					O treinador acompanhará continuamente o orientando por toda jornada de preparação, trabalhando e corrigindo questões de ordem técnica, de planejamento estratégico, tático e operacional, questões motivacionais e emocionais, estimulando o self coaching (autoconhecimento) e proporcionando a aprendizagem acelerada e sedimentada. 
				</p>
				<p>
					No intuito de explorar e controlar todos os passos do Coachee, a fim de buscar a máxima eficiência no processo, o Coaching Concurseiros desenvolveu a mais moderna e completa plataforma de estudos do país. Esse sistema é capaz de monitorar, em tempo real, as performances dos orientandos sob vários aspectos e, utilizando-se de técnicas estatísticas avançadas, constatar previamente deficiências de rendimentos em qualquer assunto estudado, controlar rigorosamente o cumprimento das metas de estudo, acompanhar, quantificar e comparar a evolução do aluno em cada matéria e no geral, gerar relatórios estruturados de desempenho a qualquer tempo, monitorar em detalhes os desempenhos em todos os simulados, realizar o acompanhamento de todas as questões realizadas - objetivas e discursivas. 
				</p>
