				<p>
					Coaching é uma relação de parceria que revela/liberta o potencial das pessoas de forma a maximizar os seus desempenhos. É ajudá-las a aprender, fazendo uso de metodologias, processos e pensamento sistêmico. 
				</p>
				<p>
					Mentoring é a arte de uma pessoa experiente ajudar outra menos experiente. O mentor é um guia, um mestre, conselheiro, alguém que tem vasta experiência profissional no campo de trabalho da pessoa que está sendo auxiliada. 
				</p>
				<p>
					Mentoring-Coaching é um processo que abrange as duas modalidades definidas acima. Por possuírem características e qualidades bastante distintas, combiná-las muitas vezes é a melhor forma de conseguir os melhores resultados. Um profissional que domine as competências de Coaching e de Mentoring terá todas as armas ao seu alcance para conseguir trazer à tona o melhor de seu aluno. 
