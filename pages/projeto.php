					Dessa forma, a qualquer momento o mentor será capaz de verificar a situação de seus aprendizes nos mínimos detalhes, assim como a coordenação pedagógica terá a visão de todos os alunos da consultoria e acompanhará e controlará em tempo integral o trabalho dos mentores e o andamento de todas as turmas. 
				</p>
